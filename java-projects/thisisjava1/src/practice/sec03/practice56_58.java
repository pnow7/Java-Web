//=, +=, % 연산자에 대해서 설명해 주세요
//1. = : 특정한 상수 값이나 변수 값 또는 객체를 변수에 전달하여 기억시킬 때 사용하는 연산자인 대입연산자이며 연사자를 중심으로 오른쪽 변수값을 왼쪽 변수에 대입한다 
//2. += : 똑같이 대입연산자이며 왼쪽 변수에 더하면서 대입한다 풀어쓰면 변수 = 변수 + 변수1 과 같다
//3. % : 산술연산자이며 두 수를 나눈 후 그 나머지를 반환한다.

//58
//++전위, 후위 연산자에 대해서 설명해 주세요
//++ : 증감연산자이며 피연산자의 값을 1씩 증가 또는 감소시키는 연산자이다. 증감연산자가 변수 앞에 위치하느냐 변수 뒤에 위치하느냐에 따라 결과 값이 달라진다
//x++ : 해당 연산을 먼저 수행한 후 피연산자의 값을 1증가 시킴
//++x : 먼저 피연산자의 값을 1증가 시킨 후 해당 연산을 수행함



